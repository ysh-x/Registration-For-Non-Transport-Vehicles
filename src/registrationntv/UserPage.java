    private void LoginBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginBtnMouseClicked
        // TODO add your handling code here:
        
        try {
            String user = UserName.getText();
            String Password = UserPassWord.getText();
            String Pwd = encrypt(Password);
            if(user.length()!=16){
                    JOptionPane.showMessageDialog(this,"\n Enter A Valid Aadhar Number \n","ERROR",JOptionPane.ERROR_MESSAGE);
            }
            else {
                 String url = "jdbc:h2:~/test";
                 Connection C = DriverManager.getConnection(url,"sa","sa");
                 String query = "SELECT NAME,COUNT(*) FROM NTVLOGIN WHERE USERNAME = '"+user+"' AND PASSWORD = '"+Pwd+"';";
                   Statement S = C.createStatement(); 
                   ResultSet RS = S.executeQuery(query);
                   
                    while(RS.next()) {
            String name = RS.getString("Name");
            if(RS.getInt("count(*)") != 0 ) {
                  showMessageDialog(this,"\t\t Welcome, "+name,"Login Successful",JOptionPane.INFORMATION_MESSAGE);
                  new UserHome().setVisible(true);
                   this.dispose();
            }
              else {
                    showMessageDialog(this,"\t\t Wrong username or Password","Login Unsuccessful",JOptionPane.ERROR_MESSAGE);

                 }
            }
              S.close();
              RS.close();
            }
        }catch(SQLException e) {
        System.out.print(e);
        
    }
    }//GEN-LAST:event_LoginBtnMouseClicked

