     public static String encrypt(String Key) {
	      char[] key = Key.toCharArray();
        for(int i = 0; i < key.length; i++) {
            switch(key[i]) {
                case 'a': {
                    key[i] = '!';
                    break;
                }
                 case 'e': {
                    key[i] = '#';
                    break;
                }
                 case 'i': {
                    key[i] = '%';
                    break;
                }
                 case 'o': {
                    key[i] = '&';
                    break;
                }
                 case 'u': {
                    key[i] = '(';
                    break;
                }
                 case '1': {
                    key[i] = 'i';
                    break;
                } case '2': {
                    key[i] = 'h';
                    break;
                } case '3': {
                    key[i] = 'g';
                    break;
                } case '4': {
                    key[i] = 'f';
                    break;
                } case '5': {
                    key[i] = 'e';
                    break;
                } case '6': {
                    key[i] = 'd';
                    break;
                } case '7': {
                    key[i] = 'c';
                    break;
                } case '8': {
                    key[i] = 'b';
                    break;
                } case '9': {
                    key[i] = 'a';
                    break;
                }case '0': {
                    key[i] = 'j';
                    break;
                }
                
                
            }
        }
        return String.valueOf(key);
    }
